package com.shraddha.test;

public class SwitchExample2 {
	public static void main(String[] args) {
		int number = 10;
		switch (number) {
		case 10:
			System.out.println("10");
			break;

		case 20:
			System.out.println("20");

		case 30:
			System.out.println("30");
		default:
			System.out.println("not in 10,20or30");
		}
	}

}
